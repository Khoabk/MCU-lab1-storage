int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  /* USER CODE BEGIN 2 */

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */





  int second=0;
  int second_pos=0;


  int minute=0;
  int minute_pos=0;

  int hour=0;




  int led_pos[12][2]={{6,9},{7,9},{8,10},{8,11},
  					{8,12},{7,13},{6,13},{5,13},
  					{4,12},{4,11},{4,10},{5,9}};


  int offset=990;

  while (1)
  {
	  //the one_hot function returns the position of the led of the corresponding specific time
	  second_pos=one_hot(second);

	  minute_pos=one_hot(minute);

	  int count=0;


	  //This while loop is to perform led matrix sweeping technique in 1 seccond
	  while(count<1000-offset)
	  {

		  int get= count%5;
		  uint32_t get2=0;
		  uint32_t get3=0;
		  GPIOA->ODR |= (31<<9);
		  GPIOA->ODR &= ~(31<<4);


			  get2=(get==led_pos[second_pos][0]-4)?1<<led_pos[second_pos][0]:get2;
			  get3=(get==led_pos[second_pos][0]-4)?1<<led_pos[second_pos][1]:get3;

			  get2=(get==led_pos[minute_pos][0]-4)?1<<led_pos[minute_pos][0]|get2:get2;
			  get3=(get==led_pos[minute_pos][0]-4)?1<<led_pos[minute_pos][1]|get3:get3;

			  get2=(get==led_pos[hour][0]-4)?1<<led_pos[hour][0]|get2:get2;
			  get3=(get==led_pos[hour][0]-4)?1<<led_pos[hour][1]|get3:get3;


			  if(get2!=0)
			  {
				  GPIOA->ODR |= get2;
				  GPIOA->ODR &= ~get3;
			  }

		 HAL_Delay(1);

		 count++;
	  }



		  second++;


		  if(second==60){second=0;   minute++;  }

		  if(minute==60){ minute=0;  hour++;   if(hour==12)hour=0; }



    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

